_main:
    pushl	%ebp
    pushl	%ebx
    pushl	%esi
    pushl	%edi
    movl	%esp, %ebp
    andl	$-16, %esp
    subl	$8, %esp
    movl	$1.5, 4(%esp)
    movl	4(%esp), %eax
    movl	%eax, 0(%esp)
    call	_getchar
